char	*ft_strlowcase(char *str)
{
	int c;

	c = 0;
	while (str[c] != '\0')
	{
		if (str[c] > 64 && str[c] < 91)
		{
			str[c] = str[c] + 32;
		}
		c++;
	}
	return (str);
}
