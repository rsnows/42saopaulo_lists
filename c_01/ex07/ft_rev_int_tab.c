void	ft_rev_int_tab(int *tab, int size)
{
	int c;
	int d;
	int tmp;

	c = 0;
	d = size - 1;
	while (c <= d)
	{
		tmp = tab[c];
		tab[c] = tab[d];
		tab[d] = tmp;
		c++;
		d--;
	}
}
