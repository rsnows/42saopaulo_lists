#include <unistd.h>

void		ft_putnbr(int n)
{
	char print;

	if (n == -2147483648)
	{
		write(1, "-", 10);
	}
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		if (n / 10)
		{
			ft_putnbr(n / 10);
		}
		print = n % 10 + '0';
		write(1, &print, 1);
	}
}
